const a = 7
let b = 3

// += significa que o valor de a está sendo acrescentado ao valor de b, como se fosse uma adição (3 + 7)
b += a // b = b + a
console.log(b);

b -= 4 // b = b - 4 (b = 10 - 4)
console.log(b);

b *= 2 // b = b * 4 (b = 6 * 2)
console.log(b);

b /= 2 // b = b / 4 (b = 12 / 2)
console.log(b);

b %= 2 // b = b % 2 (b modulo de 2 (resto da divição da ooperação))
console.log(b);